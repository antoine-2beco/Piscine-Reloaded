#include <stdio.h>

int	ft_recursive_factorial(int nb)
{
	int	result;

	result = 1;
	if (nb > 0)
	{
		result *= nb * ft_recursive_factorial(nb - 1);
		nb -= 1;
	}
	if (nb < 0)
		return (0);
	if (result > 479001600)
		return (0);
	return (result);
}

int	main(void)
{
	printf("%d", ft_recursive_factorial(13));
}